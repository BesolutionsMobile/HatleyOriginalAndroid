package hatelyoriginal.besolutions.com.hatleyoriginal.Scenarios.ClientScenarios.GetOffersScenario.Models;

public class Offer {

    int offerID;
    String offerStarName;
    String offerStarImage;
    int offerStarRate;
    int offerStarOrdersCount;
    double offerPrice;
    String offerTime;

    public int getOfferID() {
        return offerID;
    }

    public void setOfferID(int offerID) {
        this.offerID = offerID;
    }

    public String getOfferStarName() {
        return offerStarName;
    }

    public void setOfferStarName(String offerStarName) {
        this.offerStarName = offerStarName;
    }

    public String getOfferStarImage() {
        return offerStarImage;
    }

    public void setOfferStarImage(String offerStarImage) {
        this.offerStarImage = offerStarImage;
    }

    public int getOfferStarRate() {
        return offerStarRate;
    }

    public void setOfferStarRate(int offerStarRate) {
        this.offerStarRate = offerStarRate;
    }

    public int getOfferStarOrdersCount() {
        return offerStarOrdersCount;
    }

    public void setOfferStarOrdersCount(int offerStarOrdersCount) {
        this.offerStarOrdersCount = offerStarOrdersCount;
    }

    public double getOfferPrice() {
        return offerPrice;
    }

    public void setOfferPrice(double offerPrice) {
        this.offerPrice = offerPrice;
    }

    public String getOfferTime() {
        return offerTime;
    }

    public void setOfferTime(String offerTime) {
        this.offerTime = offerTime;
    }
}
